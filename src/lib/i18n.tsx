interface I18nCtx {
  locale: LocaleCode;
  setLocale: (l: LocaleCode) => void;
  t: (key: string, ...args: (string | number)[]) => string;
  wowheadPrefix: string;
}

const I18nContext = createContext<I18nCtx>({
  locale: "fr",
  setLocale: () => {},
  t: (k) => k,
  wowheadPrefix: "fr",
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<LocaleCode>("fr");

  useEffect(() => {
    const saved = localStorage.getItem("locale") as LocaleCode | null;
    if (saved && LOCALES.some((l) => l.code === saved)) {
      setLocaleState(saved);
    }
  }, []);

  const setLocale = useCallback((l: LocaleCode) => {
    setLocaleState(l);
    localStorage.setItem("locale", l);
    document.documentElement.lang = l;
  }, []);

  // Refresh Wowhead tooltips AFTER React re-renders with new locale-aware URLs
  const firstMount = useRef(true);
  useEffect(() => {
    if (firstMount.current) { firstMount.current = false; return; }
    if (typeof window === "undefined" || !(window as any).$WowheadPower) return;
    // Remove processed-link markers so Wowhead re-scans them with new URLs
    document.querySelectorAll("[data-wh-icon-added]").forEach((el) =>
      el.removeAttribute("data-wh-icon-added")
    );
    document.querySelectorAll("[data-wh-renamelink]").forEach((el) =>
      el.removeAttribute("data-wh-renamelink")
    );
    (window as any).$WowheadPower.refreshLinks();
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [locale]);

  const t = useCallback(
    (key: string, ...args: (string | number)[]): string => {
      // Try current locale, then fallback to fr, then en, then return key
      const dict = translations[locale];
      const frDict = translations.fr;
      const enDict = translations.en;
      let str = dict[key] || frDict[key] || enDict[key] || key;
      // Replace {0}, {1}, etc.
      args.forEach((arg, i) => {
        str = str.replace(`{${i}}`, String(arg));
      });
      return str;
    },
    [locale]
  );

  const wowheadPrefix = LOCALES.find((l) => l.code === locale)?.wowhead ?? "fr";

  return (
    <I18nContext.Provider value={{ locale, setLocale, t, wowheadPrefix }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}

/** Build a Wowhead item URL using the current locale */
export function useWowheadUrl() {
  const { wowheadPrefix } = useI18n();
  return (itemId: number) => {
    const prefix = wowheadPrefix ? `/${wowheadPrefix}` : "";
    return `https://www.wowhead.com${prefix}/item=${itemId}`;
  };
}
